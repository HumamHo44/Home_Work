///Question: Identify the data type of each of the following values in Dart: - 3.14 - "Hello" - true - 42
///Answer:
/// 3.14 =  double
/// "Hello" = String
/// true = bool
/// 42 =  int

void main() {
  double num = 3.14;
  String name = "Hello";
  bool value = true;
  int age = 42;
  print(num);
  print(name);
  print(value);
  print(age);
}
