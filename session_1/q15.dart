/// Question:  What does it mean for Dart to be a statically-typed language, and how does it
/// impact the way variables are used?
/// 
/// Answer:
///   Dart to be a statically-typed language The statically typed languages are those which check the variable types at compile time.
///   When defining a variable, its type must be known and constant throughout the duration of the program.
///   Its effect on the use of variables:
///   1_ The type of the variable cannot be changed after it is declared.
///   2_ Reduce bugs and improve performance